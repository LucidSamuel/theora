import {
  CurveConfig,
  CurvePoint,
  addPoints,
  scalarMultiply,
  enumerateCurvePoints,
  mod,
  DEFAULT_CURVE,
} from './logic';

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export interface PairingConfig {
  curve: CurveConfig;
  generator: CurvePoint; // base point G
  groupOrder: number; // order of the subgroup generated by G
}

export interface PairingResult {
  inputP: CurvePoint | null;
  inputQ: CurvePoint | null;
  outputValue: number; // element in the "target group" Z_n
  pScalar: number; // discrete log of P w.r.t. G (or -1)
  qScalar: number; // discrete log of Q w.r.t. G (or -1)
}

export interface BilinearityDemo {
  a: number;
  b: number;
  P: CurvePoint;
  Q: CurvePoint;
  aP: CurvePoint | null;
  bQ: CurvePoint | null;
  ePQ: number; // e(P, Q)
  eaPbQ: number; // e(aP, bQ)
  ePQab: number; // e(P,Q)^(ab) mod n
  bilinearityHolds: boolean;
}

export interface VerificationEquation {
  name: string;
  description: string;
  lhs: { pairings: { P: string; Q: string }[] };
  rhs: { pairings: { P: string; Q: string }[] };
  lhsValue: number;
  rhsValue: number;
  holds: boolean;
}

