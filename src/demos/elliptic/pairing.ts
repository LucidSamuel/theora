/** Modular exponentiation: base^exp mod m (non-negative exp). */
function modPow(base: number, exp: number, m: number): number {
  if (m === 1) return 0;
  let result = 1;
  base = mod(base, m);
  let e = exp;
  while (e > 0) {
    if (e & 1) result = mod(result * base, m);
    e >>= 1;
    base = mod(base * base, m);
  }
  return result;
}

/**
 * Find a primitive root (generator) of Z_n* where n is prime.
 * Falls back to the smallest primitive root.
 */
function findPrimitiveRoot(n: number): number {
  if (n <= 1) return 1;
  if (n === 2) return 1;

  // Factor n-1
  const phi = n - 1;
  const factors: number[] = [];
  let temp = phi;
  for (let d = 2; d * d <= temp; d++) {
    if (temp % d === 0) {
      factors.push(d);
      while (temp % d === 0) temp = Math.floor(temp / d);
    }
  }
  if (temp > 1) factors.push(temp);

  // Find smallest primitive root
  for (let g = 2; g < n; g++) {
    let isPrimitive = true;
    for (const f of factors) {
      if (modPow(g, Math.floor(phi / f), n) === 1) {
        isPrimitive = false;
        break;
      }
    }
    if (isPrimitive) return g;
  }

  return 2; // fallback — should not reach here for a valid prime
}

/**
 * Compare two curve points for equality (both null = equal).
 */
function pointsEqual(
  a: CurvePoint | null,
  b: CurvePoint | null,
): boolean {
  if (a === null && b === null) return true;
  if (a === null || b === null) return false;
  return a.x === b.x && a.y === b.y;
}

